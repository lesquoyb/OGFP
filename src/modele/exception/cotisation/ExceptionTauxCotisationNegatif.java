package modele.exception.cotisation;

public class ExceptionTauxCotisationNegatif extends Exception{

	public ExceptionTauxCotisationNegatif() {
		super("Le taux doit être positif.");
	}

	public ExceptionTauxCotisationNegatif(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ExceptionTauxCotisationNegatif(String message, Throwable cause) {
		super(message, cause);
	}

	public ExceptionTauxCotisationNegatif(String message) {
		super(message);
	}

	public ExceptionTauxCotisationNegatif(Throwable cause) {
		super(cause);
	}

}
