package modele.exception.cotisation;

public class ExceptionTauxCotisationString extends Exception{

	public ExceptionTauxCotisationString() {
		super("Le taux de cotisation doit être un nombre.");
	}

	public ExceptionTauxCotisationString(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public ExceptionTauxCotisationString(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExceptionTauxCotisationString(String arg0) {
		super(arg0);
	}

	public ExceptionTauxCotisationString(Throwable arg0) {
		super(arg0);
	}

}
